public class User {

    private String userName;
    private long userId;

    public void setUserName(String userName) {
        this.userName = userName;
    }
    public void setUserId(long id) {
        this.userId = id;
    }

    public String getUserName() {
        return this.userName;
    }
    public long getUserId() {
        return this.userId;
    }

}
